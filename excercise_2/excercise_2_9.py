# 연습문제 2-9
print('첫 번째 정수를 입력하세요.')
user_number_a = int(input())

print('두 번째 정수를 입력하세요.')
user_number_b = int(input())

print()
print(user_number_a, '+', user_number_b, '=', user_number_a + user_number_b)
print(user_number_a, '-', user_number_b, '=', user_number_a - user_number_b)
print(user_number_a, '*', user_number_b, '=', user_number_a * user_number_b)
print(user_number_a, '/', user_number_b, '=', user_number_a / user_number_b)
